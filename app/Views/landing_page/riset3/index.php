<section id="sixSec" class="offset-header">
    <div class="container-fluid pb-5 mb-3 mt-5">
        <div class="row">
            <div class="col" data-aos="fade-up" data-aos-duration="1000">
                <h3 class="headline text-center">Dimensi Utama</h3>
            </div>
        </div>
        <div class="row konten six align-items-center">
            <div class="col-md-6 text-center" data-aos="zoom-in" data-aos-duration="1000">
                <h3 class="dimensi d-none d-md-block my-3">Dimensi 1</h3>
            </div>
            <div class="isi-dimensi col-md-6 bg-light shadow box my-3" data-aos="fade-right" data-aos-duration="1000">
                <p class="sub-judul text-uppercase text-start d-md-block">Dimensi Ketahanan Pangan</p>
                <p class="isi text-justify">Pertanian urban memiliki kaitan yang erat terhadap konsep ketahanan pangan. Semakin pesatnya perkembangan pertanian urban di daerah perkotaan maka dapat menunjang kebutuhan pangan. Konsep ketahanan pangan menurut Undang-undang Nomor 7 tahun 1996 adalah kondisi terpenuhinya pangan bagi rumah tangga yang tercermin dari tersedianya pangan yang cukup, baik jumlah maupun mutunya, aman, merata, dan terjangkau. Menurut Badan Ketahanan Pangan (2006), ketahanan pangan terwujud apabila secara umum telah terpenuhi dua aspek sekaligus. Pertama adalah tersedianya pangan yang cukup dan merata untuk seluruh penduduk. Kedua setiap penduduk mempunyai akses fisik dan ekonomi terhadap pangan untuk memenuhi kecukupan gizi guna menjalani kehidupan yang sehat dan produktif dari hari ke hari.</p>
                <p class="isi text-justify">BPS mengukur ketahanan pangan menggunakan indeks yang disebut Indeks Ketahanan Pangan (IKP). Ketahanan pangan mencakup ketersediaan pangan, keterjangkauan/akses pangan, dan pemanfaatan pangan. Sesuai dengan cakupan BPS, dimensi ketahanan pangan pada pertanian urban ini akan diukur melalui hasil produksi, distribusi hasil produksi, dan motivasi terbentuknya community gardening serta ketersediaan pangan, keterjangkauan pangan, dan pemanfaatan pangan pada rumah tangga pelaku pertanian urban.</p>
            </div>
            <div class="isi-dimensi col-md-6 bg-light shadow box my-3" data-aos="fade-right" data-aos-duration="1000">
                <p class="sub-judul text-uppercase text-end d-md-block">Dimensi Ekonomi</p>
                <p class="isi text-justify">Pertanian urban menghasilkan produk pertanian yang berkaitan dengan aspek ekonomi sehingga memberikan penghasilan bagi pelaku pertanian urban. Ekonomi pertanian merupakan aktivitas yang berhubungan dengan bagaimana produsen, konsumen, dan masyarakat menggunakan sumber daya yang terbatas dalam produksi, pengolahan, pemasaran, dan konsumsi produk pangan dan serat, (Haryanto, et al., 2009). Hal ini berarti dimensi ekonomi yang dicakup adalah aktivitas ekonomi yang terkait dengan produksi, distribusi, dan konsumsi produk dan jasa pertanian.</p>
                <p class="isi text-justify">Mayoritas penjualan hasil pertanian urban dilakukan melalui berbagai saluran pemasaran langsung. Pertanian urban memiliki karakteristik yakni dekat dengan pasar pertaniannya. Pasar pertanian urban, diantaranya supermarket (grocery store), restoran, dan toko makanan organik (EDRS, 2013).</p>
                <p class="isi text-justify">Selain itu, pada aspek ekonomi pertanian urban juga berbicara tentang biaya pertanian. “Seperti semua bisnis, biaya pertanian urban bersifat tetap (konstan terlepas dari volume produksi) atau variabel (berfluktuasi dengan volume produksi). Kategori biaya pertanian urban meliputi tenaga kerja; input produksi; dan penjualan, pemasaran, dan administrasi”(Dorward, et al, 2013:40). Menurutnya, biaya tenaga kerja merupakan kategori biaya terbesar karena pada umumnya teknologi yang digunakan pada pertanian urban masih bergantung pada tenaga kerja manual. Input produksi yang digunakan meliputi tanah, pembenahan tanah, benih, prasarana irigasi, pupuk, peralatan, dan lain-lain. Para petani cenderung membatasi transaksi tunai untuk menghindari risiko arus kas yang sulit. Selain itu, pada penjualan, pemasaran, dan administrasi tidak secara langsung berhubungan dengan pertumbuhan, tetapi sangat penting untuk operasi bisnis misalnya biaya telepon dan komunikasi, ongkos kirim, dan perlengkapan kantor.</p>
            </div>
            <div class="col-md-6 text-center" data-aos="zoom-in" data-aos-duration="1000">
                <h3 class="dimensi d-none d-md-block my-3 ">Dimensi 2</h3>
            </div>
            <div class="col-md-6 text-center" data-aos="zoom-in" data-aos-duration="1000">
                <h3 class="dimensi d-none d-md-block my-3">Dimensi 3</h3>
            </div>
            <div class="isi-dimensi col-md-6 bg-light shadow box my-3" data-aos="fade-right" data-aos-duration="1000">
                <p class="sub-judul text-uppercase text-start d-md-block">Dimensi Sosial Demografi</p>
                <p class="isi text-justify">Karakteristik sosial demografi adalah ciri yang menggambarkan perbedaan masyarakat berdasarkan umur, jenis kelamin, pekerjaan, pendidikan, agama, suku bangsa, pendapatan, jenis keluarga, status pernikahan, lokasi geografi, dan kelas sosial (Kotler dan Armstrong, 2001).</p>
                <p class="isi text-justify">Dalam penelitian ini karakteristik sosial demografi pelaku pertanian urban meliputi umur, jenis kelamin, dan tingkat pendidikan. Termasuk juga jenis pekerjaan utama, pengalaman melakukan pertanian urban, tujuan melakukan pertanian urban dan lamanya tinggal di perkotaan (Ayu; Sebayang; Iskandar, 2018).</p>
            </div>
            <div class="isi-dimensi col-md-6 bg-light shadow box my-3" data-aos="fade-right" data-aos-duration="1000">
                <p class="sub-judul text-uppercase text-end d-md-block">Dimensi Lingkungan</p>
                <p class="isi text-justify">Pertanian urban sangat berkaitan dengan dimensi lingkungan. Pertanian urban menawarkan solusi untuk menciptakan lahan terbuka hijau di tengah padatnya bangunan perkotaan. Pemanfaatan limbah organik pada pertanian urban juga secara efektif akan mengurangi penggunaan pupuk anorganik dan mendorong pertanian ramah lingkungan berkelanjutan. Selain pemanfaatan limbah organik, pertanian urban juga dapat mengurangi limbah anorganik. Hal ini dilakukan dengan cara memanfaatkan limbah anorganik sebagai media bertani. Penerapan pertanian urban ini memperlihatkan bahwa pengelolaan limbah memberikan manfaat ekonomi dan dapat mengubah perilaku masyarakat dalam memanfaatkan sampah. Sehingga, dalam penelitian ini akan dilihat bagaiman pelaku pertanian urban di Kota Bandung dan Kota Tasikmalaya mengelola lahan dan limbah yang dihasilkannya.</p>
            </div>
